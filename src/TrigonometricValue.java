public class TrigonometricValue {


public static void main(String args[]){


double angle=45;


 
        double sineOfNum = Math.sin(angle);
        double cosineOfNum = Math.cos(angle);
        double tangentOfNum = Math.tan(angle);
 
 
        System.out.println("sin("+angle+") = "+sineOfNum);
        System.out.println("cos("+angle+") = "+cosineOfNum);
        System.out.println("tan("+angle+") = "+tangentOfNum);
 
        
 
       




}

    
}
